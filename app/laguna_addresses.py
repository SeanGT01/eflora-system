def get_municipalities():
    """Get list of all municipalities in Laguna"""
    return sorted(LAGUNA_ADDRESSES.keys())

def get_barangays(municipality):
    """Get list of barangays for a specific municipality"""
    if municipality in LAGUNA_ADDRESSES:
        return sorted(LAGUNA_ADDRESSES[municipality]["barangays"])
    return []

def get_coordinates(municipality):
    """Get approximate coordinates for a municipality"""
    if municipality in LAGUNA_ADDRESSES:
        return LAGUNA_ADDRESSES[municipality]["coordinates"]
    return None

def format_address(municipality, barangay, street=None, building_details=None):
    """Format a complete address string"""
    parts = []
    
    if building_details:
        parts.append(building_details)
    if street:
        parts.append(street)
    
    parts.append(f"Barangay {barangay}")
    parts.append(municipality)
    parts.append("Laguna")
    parts.append("Philippines")
    
    return ", ".join(parts)